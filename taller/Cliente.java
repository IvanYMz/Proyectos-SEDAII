package MP;

public class Cliente {
    private int Id;
    private String Name;
    private String SecName;
    private String Estado;
    private String Usuario_R;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getSecName() {
        return SecName;
    }

    public void setSecName(String SecName) {
        this.SecName = SecName;
    }

    public String getEstado() {
        return Estado;
    }

    public void setEstado(String Estado) {
        this.Estado = Estado;
    }

    public String getUsuario_R() {
        return Usuario_R;
    }

    public void setUsuario_R(String Usuario_R) {
        this.Usuario_R = Usuario_R;
    }
    
}
